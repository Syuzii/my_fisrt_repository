#include "ft_printf.h"

int	is_specifier(char c)
{
	return (c == 'd' || c == 'i' || c == 'c' || c == 's'
			|| c == 'p' || c == 'u' || c == 'x' || c == 'X' || c == '%');
}

int	is_flag(char c)
{
	return (c == '0' || c == '-' || c == '.' || c == '*');
}
